# Пройдена відстань

# Постійна швидкість автомобіля.
SPEED = 70

# Розрахувати відстань, яку проїде автомобіль
# за 6, 10 і 15 годин.
dist_for_6_hours = SPEED * 6
dist_for_10_hours = SPEED * 10
dist_for_15_hours = SPEED * 15

# Виводимо на екран результати розрахунків.
print(f"За 6 годин автомобіль проїде {dist_for_6_hours} км")
print(f"За 10 годин автомобіль проїде {dist_for_10_hours} км")
print(f"За 15 годин автомобіль проїде {dist_for_15_hours} км")
