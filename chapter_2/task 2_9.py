# Конвертер градусів Цельсія у градуси Фаренгейта

# Отримання температури в градусах Цельсія.
celsius = float(input("Температура в градусах Цельсія: "))

# Розрахунок еквівалентної температури в градусах Фаренгейта.
fahrenheit = (9.0 / 5.0) * celsius + 32

# Виводимо на екран результати розрахунків.
print(f"{celsius:.1f} градусів Цельсія еквівалентно " +
      f"{fahrenheit:.1f} градусам Фаренгейта")
