# Обчислення факторіалу числа

# Ініціалізація змінних.
number = 0
factorial = 1

# Отримання додатнього числа від користувача.
while number <= 0:
    number = int(input("Введіть додатнє число: "))

# Розрахунок факторіалу числа.
for num in range(1, number + 1):
    factorial *= num

# Виводимо результати на екран.
print(f"Факторіал числа {number} дорівнює {factorial:,}")
