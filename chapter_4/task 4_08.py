# Сумма додатніх чисел

# Ініціалізація змінних.
total = 0
number = 1

# Отримання числового ряду від користувача і розрахунок суми чисел.
while number > 0:
    number = float(input("Введіть додатнє число (від'ємне, щоб вийти): "))

    # Перевіряємо чи є число додатнім.
    if number > 0:
        total += number

# Виводимо результати на екран.
print(f"\nСума введених чисел дорівнює {total:.2f}")
