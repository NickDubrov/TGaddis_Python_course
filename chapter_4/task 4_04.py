# Пройдена відстань

# Отримання середньої швидкості транспортного засобу.
speed = int(input("Швидкість транспортного засобу в км/год: "))

# Отримання кількості витраченого часу.
hours = int(input("Кількість часу в дорозі в годинах: "))

# Виводимо результати на екран.
print("\nЧас\t\tПройдена відстань")
print("---------------------------------")

for hour in range(1, hours + 1):
    distance = speed * hour
    print(hour, "\t\t", distance)
