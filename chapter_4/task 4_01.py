# Збирач помилок

# Ініціалізація накопичувальної змінної.
total = 0

# Отримання кількості помилок, зібраних протягом 5 днів.
for day in range(5):
    bugs = int(input(f"Кількість помилок, зібраних за {day+1} день: "))
    total += bugs

# Виводимо результати на екран.
print(f"За п'ять днів було зібрано {total} системних помилок.")
