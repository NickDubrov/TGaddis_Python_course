# Дрібна монета для заробітної плати

# Ініціалізація змінних.
total = 0
salary_per_day = 1

# Отримання кількості робочих днів.
days = int(input("Кількість робочих днів: "))

# Розраховуємо і виводимо результати на екран.
print("День\t\tЗарплата")
print("------------------------")


for day in range(1, days + 1):
    print(day, "\t\t", salary_per_day / 100)
    total += salary_per_day
    salary_per_day *= 2

print(f"\nЗагальна заробітна плата за {days} робочих днів "
      f"становить {total / 100:,.2f} грн.")
