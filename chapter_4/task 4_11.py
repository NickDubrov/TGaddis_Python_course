# Зниження маси тіла

# Іменовані константи.
MONTHS = 6

# Отримання початкової ваги користувача.
weight = float(input("Початкова вага: "))

# Розраховуємо і виводимо результати на екран.
print("\nМісяць\t\tПрогнозована вага")
print("---------------------------------")

for month in range(1, MONTHS + 1):
    weight -= 1.5
    print(month, "\t\t", weight)
