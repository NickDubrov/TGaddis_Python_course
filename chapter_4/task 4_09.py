# Рівень океану

# Іменовані константи.
RISE_PER_YEAR = 1.6

# Ініціалізація змінних.
rise = 0

# Розраховуємо і виводимо результати на екран.
print("Рік\t\tПідйом в мм")
print("---------------------------")

for year in range(25):
    rise += RISE_PER_YEAR
    print((year + 1), "\t\t", format(rise, ".2f"))
