# Класифікатор віку

# Отримання віку користувача.
age = int(input("Введіть свій вік: "))

# Визначення категорії користувача і вивід результатів на екран.
if age <= 1:
    print("Немовля")
elif age > 1 and age <= 13:
    print("Дитина")
elif age > 13 and age <= 20:
    print("Підліток")
else:
    print("Дорослий")
