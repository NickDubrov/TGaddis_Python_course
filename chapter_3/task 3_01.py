# День тижня

# Отримання числа.
num = int(input("Введіть число від 1 до 7: "))

# Визначення дня тижня.
if num == 1:
    print("Понеділок")
elif num == 2:
    print("Вівторок")
elif num == 3:
    print("Середа")
elif num == 4:
    print("Четвер")
elif num == 5:
    print("П'ятниця")
elif num == 6:
    print("Субота")
elif num == 7:
    print("Неділя")
else:
    print("Помилка! Введіть число від 1 до 7")
